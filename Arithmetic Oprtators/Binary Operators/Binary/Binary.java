import java.util.*;

public class Binary {
    public static void main(String args[]) {

        int A = 10;
        int B = 5;

        System.out.println("Sum = " + (A + B));
        System.out.println("Subtraction = " + (A - B));
        System.out.println("Multiplication = " + (A * B));
        System.out.println("Modulo(Remider) = " + (A % B));
    }
}