import java.util.*;

public class Counting {
    public static void main(String args[]) {

        // Print number from 1 to 10

        int Counting = 1;

        System.out.println("=====Counting from 1 to 10=====");
        while(Counting <= 10) {
            System.out.println(Counting);
            Counting++;
        }
    }
}