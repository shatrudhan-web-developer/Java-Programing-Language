import java.util.*;

public class SwitchStatement {
    public static void main(String args[]) {

        int number = 2;

        switch(number) {
            case 1 : System.out.println("Samosa");
                    break;

            case 2 : System.out.println("Momose");
                    break;

            case 3 : System.out.println("Jalebi");
                    break;

            default : System.out.println("We wake up");
        }
    }
}