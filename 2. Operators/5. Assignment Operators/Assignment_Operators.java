import java.util.*;

public class Assignment_Operators {
    public static void main(String agrs[]) {

        int a = 10;
        // a = a + 10;
        a += 10;

        int b = 5;
        // b = b - 5;
        b -= 5;

        int c = 3;
        // c = c * 3;
        c *= 3;

        int d = 2;
        // d = d / 2;
        d /= 2;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}